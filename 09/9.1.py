M = int(input('Сколько мальчиков? '))
D = int(input('Сколько девочек? '))
answer = ''
if (M > 2 * D) or (D > 2 * M):
    print('Рассадить по условию не получится!\nРешений нет!')
elif M >= D:
    MDM = M - D
    for mdm in range(MDM):
        answer += 'MDM '
    MD = D - MDM
    for md in range(MD):
        answer += 'MD '
else:
    DMD = D - M
    for dmd in range(DMD):
        answer += 'DMD '
    DM = M - DMD
    for dm in range(DM):
        answer += 'DM '
print(answer)

